package operators;

public class ArithmaticOperator {
	// ArithmaticOperators:

		// + Additive operator
		// - subtraction
		// * Multiplication
		// / Division
		// % Remainder
	public static void main(String[] args) {
		int age1 = 100;
		int age2 = 50;
		int sum = age1 + age2;
		int sub = age1 - age2;
		int multi = age1 * age2;
		int division = age1 / age2;
		int remainder = age1 % age2;

		System.out.println("Summation value is " + sum);
		System.out.println("Subtract value is " + sub);
		System.out.println("Multiply value is " + multi);
		System.out.println("Dision value is " + division);
		System.out.println("Remainder value is " + remainder);

	}

}
